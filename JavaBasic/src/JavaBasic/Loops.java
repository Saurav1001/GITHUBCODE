package JavaBasic;

public class Loops {

	public static void main(String[] args) {
		//1 to 10
		int i=1;
		while(i<5) {
			//System.out.println(i);
			i=i+1;
		}
		//1 to 10
		for(int j=1; j<10; j++) {
			//System.out.println(j);
		}
		//10 to 1
		int a=10;
		while(a>=1) {
			//System.out.println(a);
			a=a-1;
			
		}
		for(int b=10; b>=1;b--) {
			System.out.println(b);
		}

	}

}
